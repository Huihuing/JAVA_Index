package day1;

public class VariableExam1  {
    public static void main(String[] args) {

        // 정수 : int, 실수(소수) : double, 문장 : String
        int Examnum;
        Examnum = 100;

        double Examble;
        Examble = 3.14;

        String Exameng;
        Exameng = "안녕하세요";

        System.out.println(Examnum); // 출력 : 100

        System.out.println(Examble); // 출력 : 3.14

        System.out.println(Exameng); // 출력 : 안녕하세요

        Examnum = 200;
        Examble = 10.5;
        Exameng = "반갑습니다.";

        System.out.println(Examnum); // 출력 : 200

        System.out.println(Examble); // 출력 : 10.5

        System.out.println(Exameng); // 출력 : 반갑습니다.
    }
}
